// greet라는 함수를 작성하세요
// 이 함수는 단일 문자열 또는 문자열 배열을 인자로 받을 수 있습니다
// 단일 문자열인 경우 "Hello, <name>" 형식으로 출력해야 하고
// 문자열 배열인 경우 배열의 각 사람에게 동일한 형식으로 인사해야 합니다

const greet = (item: string | string[]) => {
  if (typeof item === "string") {
    console.log(`hello, <${item}>`);
    return;
  }
  item.forEach((e) => {
    console.log(`hello, <${e}>`);
  });
};

greet("hi");
